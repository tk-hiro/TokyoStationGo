// 未定義の路線IDから彩度・明度を固定した色を安定生成する
function fallbackColor(lineId: number): string {
  const hue = (lineId * 137) % 360
  return `hsl(${hue}, 65%, 45%)`
}

export function getLineColor(lineId: number): string {
  return LINE_COLORS[lineId] ?? fallbackColor(lineId)
}
